// 家长端：通用沟通建议 + 信息防火墙
// 严格遵守：AI 不能透露孩子未授权的具体事实。
// 如果孩子没有授权分享具体内容，家长端只能得到通用沟通建议。
// AI 不能说"孩子其实告诉我……"，不能暗示孩子具体说了什么、经历了什么。

import type { ParentGuide } from '../types'

// 家长端通用建议（不依赖孩子具体内容）
export function buildParentGuide(_parentQuestion: string): ParentGuide {
  return {
    firewallNote:
      '我不能透露孩子没有允许分享的内容。以下建议是基于通用沟通方式给出的，不涉及孩子具体说了什么。',
    iceBreaker:
      '先不要追问具体原因，可以先表达你愿意听。比如找一个不被打扰的时间，说一句："我注意到你最近不太开心，我不会逼你说，但如果你愿意，我可以先听你讲十分钟。"',
    sayThree: [
      '我先听你说完，不急着评价。',
      '你说的我都记下了，谢谢你愿意告诉我。',
      '我们慢慢来，不用一次说完。',
    ],
    notSayThree: [
      '别想那么多，没什么大不了的。',
      '别人家孩子不也好好的吗。',
      '你就是太矫情/太懒了。',
    ],
    nextStep:
      '先听孩子说完，约定一个不被打扰的谈话时间；观察接下来几天的状态；如果持续低落或提到伤害自己，联系学校心理老师或专业人员。',
    replyDraft:
      '我注意到你最近不太开心，我不会逼你说，但如果你愿意，我可以先听你讲十分钟。什么时候方便都可以，我等你。',
  }
}

// 防火墙拒绝模板：家长追问孩子具体内容时使用
