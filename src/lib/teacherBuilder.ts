// 老师端：谈话建议 + 观察点 + 转介
// 严格遵守：不做诊断，不自动告知家长，只帮老师整理如何保护隐私地开启谈话。

import type { TeacherGuide } from '../types'

// 老师端建议（独立流程，不接收孩子消息）
export function buildTeacherGuide(_observation: string): TeacherGuide {
  return {
    summary:
      '学生近期可能正在经历一些压力，表现出需要被关注但尚未明确表达的信号。这不是诊断，而是一个值得留意的提示。',
    privacyNote:
      '不要在班级里提及这次谈话，不要公开化处理。学生提到的内容未经同意不告知其他老师或家长。',
    talkAdvice: [
      '找一个不会被同学听见的时间，比如课后单独留一两分钟。',
      '先听学生说完，不打断、不公开化、不当众询问。',
      '不要在班级里提及这次谈话，保护隐私。',
    ],
    observePoints: [
      '这种状态持续了多久，是偶尔还是持续。',
      '到校情况、是否请假、是否回避特定课程。',
      '睡眠和精神状态是否明显变化。',
      '同伴关系是否有异常，是否被孤立。',
      '学业压力是否突然加重。',
      '和家庭沟通的情况（如学生提及）。',
    ],
    referAdvice:
      '如果这种状态持续出现，或者学生提到伤害自己、不想活等表达，请联系学校心理老师、家长或专业支持渠道；不要独自承担判断责任。',
  }
}
